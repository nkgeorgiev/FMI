    private void detokenizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detokenizeActionPerformed
        //try to find the card number corresponding to the given token
        String token = txtToken2.getText();
        if(!token.matches("[\\d\\s]*")){
            JOptionPane.showMessageDialog(rootPane, "token must contain only digits and whitespaces");
            return;
        }
        String number = "";
        try {
            number = rmiServer.detokenize(token);
            txtNumber2.setText(number);
        } catch (InvallidTokenException ex) {
            JOptionPane.showMessageDialog(rootPane, "Token doesn't map to any card number");
        } catch (RemoteException ex) {
            System.out.println("remoteException");
        }

    }//GEN-LAST:event_detokenizeActionPerformed

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        //try to register a new account
        String username = txtUsernameReg.getText();
        String pass = new String(txtPasswordReg.getPassword());
        String repPass = new String(txtRepPasswordReg.getPassword());
        if (username.equals("") || pass.equals("") || repPass.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "You must fill in all fields");
        } else if (!pass.equals(repPass)) {
            JOptionPane.showMessageDialog(rootPane, "Passwords doesn't match");
        } else {
            try {
                Boolean added = rmiServer.addAccount(username, pass, 1);
                if (added) {
                    JOptionPane.showMessageDialog(rootPane, "Registration Successful");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Duplicate username");
                }
                txtUsernameReg.setText("");
                txtPasswordReg.setText("");
                txtRepPasswordReg.setText("");
            } catch (RemoteException ex) {
                System.out.println("RemoteException");
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

