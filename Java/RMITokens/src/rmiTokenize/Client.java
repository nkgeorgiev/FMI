    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        //try to login via rmi 
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword());
        try {
            privilage = rmiServer.login(username, password);
        } catch (RemoteException ex) {
            System.out.println("RemoteException");
        }
        //the privilage determines which panels will show 
        if (privilage > 0) {
            lblLogin.setText("Logged in as " + username);
            LoginPanel.setVisible(false);
            btnLogout.setVisible(true);
            if (privilage >= 1) {
                tokenizePanel.setVisible(true);
            }
            if (privilage >= 2) {
                addAccountPanel.setVisible(true);
                savePanel.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Incorrect username/password");
        }
        System.out.println(privilage);
        txtUsername.setText("");
        txtPassword.setText("");


    }//GEN-LAST:event_btnLoginActionPerformed

